using System;

namespace OOP_lesson_series.Lesson_3._Interface
{
    public class Construct: HitPoints, IDamageable
    {
        private bool _isBurning = false;
        private float _multiplier = 1;
        private const float BURNING_MULTIPLIER = 1.5f;
        private readonly string _name;

        public Construct()
        {
            _name = GetType().Name;
        }

        public virtual void Damage(float amount)
        {
            if (!(Hp > 0)) return;
            var damageReceived = amount * _multiplier;
            Hp -= damageReceived;
            Console.WriteLine($"{_name} took {damageReceived} damage");
            if (Hp < 0)
                Destroy();
            else if (Hp < InitialHp / 2 && !_isBurning)
            {
                Ignite();
            }

        }

        private void Ignite()
        {
            Console.WriteLine($"{_name} got ignited. Applying x{BURNING_MULTIPLIER} damage multiplier");
            _multiplier = BURNING_MULTIPLIER;
        }

        public virtual void Destroy()
        {
            Console.WriteLine($"{_name} is destroyed");
            Hp = 0;
        }
    }
}

