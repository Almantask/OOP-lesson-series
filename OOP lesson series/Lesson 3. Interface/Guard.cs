using System;

namespace OOP_lesson_series.Lesson_3._Interface
{
    public class Guard: HitPoints, IDamageable
    {
        public void Damage(float amount)
        {
            if (Hp > 0)
            {
                Hp -= amount;
                if (Hp < 0)
                    Destroy();
            }
        }

        public void Destroy()
        {
            Console.WriteLine("Guard killed");
        }
    }
}

